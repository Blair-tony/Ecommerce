@login_required
def add_review(request, product_slug):
    item = Item.objects.get(slug=product_slug)
    if request.method == 'POST':
        form = ReviewForm(request.POST)
        if form.is_valid():
            review = form.save(commit=False)
            review.item = item
            review.user = request.user
            review.save()
            return redirect('core:product', slug=product_slug)
    else:
        form = ReviewForm()
    return render(request, 'product_detail.html', {'form': form, 'object': item})

