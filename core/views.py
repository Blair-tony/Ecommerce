class CategoryView(View):
    def get(self, *args, **kwargs):
        category = Category.objects.get(slug=self.kwargs['slug'])
        items = Item.objects.filter(category=category, is_active=True)

        # Price filtering
        price_range = self.request.GET.get('price_range')
        if price_range:
            min_price, max_price = map(float, price_range.split('-'))
            items = items.filter(price__gte=min_price, price__lte=max_price)

        # Search functionality
        query = self.request.GET.get('q')
        if query:
            items = items.filter(
                Q(title__icontains=query) |  # Search by item title
                Q(description_short__icontains=query)  # Search by item description
            )

        context = {
            'object_list': items,
            'category_title': category,
            'category_description': category.description,
            'category_image': category.image
        }
        return render(self.request, "category.html", context)



